<h1>Community Service</h1>

<p>There are many opportunities to volunteer with MRun throughout the year.  Listed here are some of the club's main community service endeavors.</p>

<h2>Relay for Life</h2>
 
<p>The Running Club participates in the U of M Relay for Life, a fundraiser for the American Cancer Society held at Palmer Field in April of each year. The idea is for every team to have someone walking around the track at all times, but naturally, as a running club, we run around the track for all 24 hours! In 2011, we walked/ran 530 miles in 24 hours. And while not running, we are singing karaoke, competing in basketball and dodgeball tournaments, and generally having a grand old time. For our spirit and dedication to the event, we have received the Most Spirited Team Award at Relay for Life in 2008, 2009, and 2010. But most importantly, through Saturday-night bake sales, on-site fundraisers (pie in the face, anyone?) and individual fundraising, we have raised over $10,000 for cancer research through the years.</p>


<h2>myTEAM TRIUMPH</h2>

<p>The running club partners with myTEAM TRIUMPH to help handicapped community members race in local road races. MyTEAM TRIUMPH is an athletic ride-along program created for children, teens, adults, and veterans with disabilities who would normally not be able to experience endurance events such as triathlons or road races. We have completed 5k and 10k races with our handicapped "team captains," and we hope to expand to half-marathons and other opportunities in the near future.</p>

<p>More information about myTEAM TRIUMPH can be found at their website: <a href="http://www.myteamtriumph.com/" target="_blank" >http://www.myteamtriumph.com/</a>
</p>


<h2>Race Across Michigan</h2>
 
<p>Race Across Michigan is an annual charity relay run beginning in Detroit along the Detroit River and finishing 182 miles west on the coast of Lake Michigan. A group of running club members cover the distance as a relay race with some members running up to 20 miles over the course of the race. Our team goal is to cover the entire distance in under 24 hours while raising money for the Special Olympics of Michigan. The event has raised over $1,500 each year and in 2013 we raised over $18,000! The event started in 2009, and has grown steadily over the years.</p>

<p><a href="http://mrun.clubrunning.org/ram/">Click here</a> for more information about this year's event, which takes place March 22-23, 2014.</p>

<h2>Nationals Shoe Drive</h2>
 
<p>MRun hosts an annual shoe drive at the NIRCA Cross Country National Championships meet. We arrange for running clubs from across the country to bring old shoes with them to the meet and have them donated to local homeless shelters. We have a friendly competition between the clubs to see who can donate the most shoes. Since the program's inception in 2008, we have put hundreds of pairs of shoes on needy feet.</p>


